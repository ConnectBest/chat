export function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState<string>('Smileys');

  return (
    <div className="fixed inset-0 z-50" onClick={onClose}>
      <div 
        className="absolute bottom-20 right-4 w-96 bg-brand-800/95 backdrop-blur-lg rounded-xl border border-white/20 shadow-xl" 
        onClick={e => e.stopPropagation()}
      >
        {/* Category Tabs */}
        <div className="flex gap-1 p-2 border-b border-white/10 overflow-x-auto scrollbar-thin">
          {Object.keys(EMOJI_CATEGORIES).map(category => (
            <button
              key={category}
              onClick={() => setActiveCategory(category)}
              className={`px-3 py-1 rounded-lg text-xs font-medium transition whitespace-nowrap ${
                activeCategory === category
                  ? 'bg-brand-500 text-white'
                  : 'text-white/60 hover:text-white hover:bg-white/10'
              }`}
            >
              {category}
            </button>
          ))}
        </div>

        {/* Emoji Grid */}
        <div className="p-3 max-h-64 overflow-y-auto scrollbar-thin">
          <div className="grid grid-cols-8 gap-1">
            {EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES].map((emoji, index) => (
              <button
                key={`${emoji}-${index}`}
                onClick={() => { onSelect(emoji); }}
                className="text-2xl hover:bg-white/10 rounded p-2 transition"
                title={emoji}
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>

        <div className="p-2 border-t border-white/10 bg-white/5">
          <p className="text-[10px] text-white/40 text-center">
            Static code Backend team please change it to dynamic
          </p>
        </div>
      </div>
    </div>
  );
}

interface ReactionBarProps {
  reactions: { emoji: string; count: number; users: string[] }[];
  onReact: (emoji: string) => void;
  onShowPicker: () => void;
}

export function ReactionBar({ reactions, onReact, onShowPicker }: ReactionBarProps) {
  return (
    <div className="flex flex-wrap gap-1 mt-1">
      {reactions.map(r => (
        <button
          key={r.emoji}
          onClick={() => onReact(r.emoji)}
          className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full bg-white/10 hover:bg-white/20 text-xs transition"
          title={r.users.join(', ')}
        >
          <span>{r.emoji}</span>
          <span className="text-white/70">{r.count}</span>
        </button>
      ))}
      <button
        onClick={onShowPicker}
        className="inline-flex items-center px-2 py-0.5 rounded-full bg-white/5 hover:bg-white/10 text-xs transition"
        aria-label="Add reaction"
      >
        <span>😀</span>
      </button>
    </div>
  );
}
